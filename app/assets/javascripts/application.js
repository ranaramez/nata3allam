function filter(element,what) {
    var value = $(element).val();
    value = value.trim()
    if(value == '')
        $('#'+what+'  li').show();
    else{
        $('#'+what+'  li:not(:contains(' + value + '))').hide();
        $('#'+what+'  li:contains(' + value + ')').show();
    }
};
